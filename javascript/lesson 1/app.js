// 21. Conditional statements with `or` operator
// If statement with logical operators
let country = "Kazakhstan";
if (country === "Kazakhstan" || country === "Russia") {
    console.log("The country is in Europe");
} else if (country === "USA" || country === "Canada") {
    console.log("The country is in North America");
} else if (country === "Australia" || country === "New Zealand") {
    console.log("The country is in Oceania");
} else if (country === "China" || country === "Japan") {
    console.log("The country is in Asia");
} else if (country === "Egypt" || country === "South Africa") {
    console.log("The country is in Africa");
} else if (country === "Brazil" || country === "Argentina") {
    console.log("The country is in South America");
} else {
    console.log("The country is not in the list");
}

